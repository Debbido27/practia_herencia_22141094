
package herencia;


public class Persona {
   String nombre;
   int edad;
   String identidad;
   String direccion;
   String telefono;
   
   public Persona(String nombre, int edad, String identidad, String direccion, String telefono){
       this.nombre=nombre;
       this.edad=edad;
       this.identidad=identidad;
       this.direccion=direccion;
       this.telefono=telefono;
   }
    
}
