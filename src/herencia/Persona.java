
package herencia;


public class Persona {
   String nombre;
   int edad;
   int identidad;
   String direccion;
   int telefono;
   
   public Persona(String nombre, int edad, int identidad, String direccion, int telefono){
       this.nombre=nombre;
       this.edad=edad;
       this.identidad=identidad;
       this.direccion=direccion;
       this.telefono=telefono;
   }
    
}
